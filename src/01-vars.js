//variables
//var, let, const

//var es una variable global, se puede redeclarar y reasignar.
var bloquesRojos = true;
var bloquesRojos = 5;

console.log(bloquesRojos);

//let es una variable local, no se puede redeclarar.
let contador = 0;
let mensaje = "Hola Mundo";
contador = 5;

console.log(contador);
console.log(mensaje);

//const es una variable constante, no se puede redeclarar ni reasignar.
const PI = 3.1416;

console.log(PI);

